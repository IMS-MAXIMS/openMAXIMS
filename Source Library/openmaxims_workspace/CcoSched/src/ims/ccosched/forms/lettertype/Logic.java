package ims.ccosched.forms.lettertype;

import ims.framework.enumerations.DialogResult;

public class Logic extends BaseLogicDTO
{
	protected void onCancelClick() throws ims.framework.exceptions.PresentationLogicException
	{
		engine.close(DialogResult.CANCEL);
	}
	protected void onOkClick() throws ims.framework.exceptions.PresentationLogicException
	{
		if(!form.PatientLetter().getValue() && !form.GPLetter().getValue())
		{
			engine.showMessage("Please select a Letter Type.");
			return;
		}

		form.getGlobalContext().CcoSched.LetterType.setGPLetter(null);
		form.getGlobalContext().CcoSched.LetterType.setPatientLetter(null);
		
		if(form.GPLetter().getValue() == true)
			form.getGlobalContext().CcoSched.LetterType.setGPLetter(Boolean.TRUE);
		if(form.PatientLetter().getValue() == true)
			form.getGlobalContext().CcoSched.LetterType.setPatientLetter(Boolean.TRUE);
		
		engine.close(DialogResult.OK);
	}
}
