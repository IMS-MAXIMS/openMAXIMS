	@Override
	protected void onMessageBoxClosed(int messageBoxId, DialogResult result) throws PresentationLogicException
	{
		if( form.getLocalContext().getlcDTAWithTransferMessageId() != null && messageBoxId == form.getLocalContext().getlcDTAWithTransferMessageId())
		{
			if( DialogResult.YES.equals(result))
			{		 
				try
				{
					if (form.getLocalContext().getTrackingForReferralIsNotNull())
					{
						form.getLocalContext().setTrackingForReferral(domain.getTrackingForReferral(form.getLocalContext().getTrackingForReferral()));
					}

					TrackingForReferalToSpecialtyVo trackingRec = null;
					if (form.getLocalContext().getTrackingForReferralIsNotNull() && form.getLocalContext().getTrackingForReferral().getCurrentStatusIsNotNull())
					{
						trackingRec = (TrackingForReferalToSpecialtyVo) form.getLocalContext().getTrackingForReferral().clone();
						
						if (trackingRec.getCurrentStatusIsNotNull() && TrackingStatus.DECISION_TO_ADMIT.equals(trackingRec.getCurrentStatus().getStatus()))
							trackingRec.setCurrentStatus(populateTrackingAttendanceStatus(form.getLocalContext().getTrackingForReferral().getCurrentStatus().getPrevStatus()));
						
						String[] errors = trackingRec.validate();
						if(errors != null && errors.length > 0)
						{
							engine.showErrors(errors);
							return;
						}
					}
					
					form.getLocalContext().setlcCurrentPartialAdmission(domain.cancelCurrentPartialAdmission(form.getLocalContext().getlcCurrentPartialAdmission(), trackingRec));
					engine.open(form.getForms().Emergency.TransferDialog, new Object[] { form.getLocalContext().getlcCurrentPartialAdmission()});
				} 
				catch (StaleObjectException e)
				{
					engine.showMessage(ConfigFlag.UI.STALE_OBJECT_MESSAGE.getValue());	
					open();
				}
			}
			else
				open();
		}
		
	}
	
