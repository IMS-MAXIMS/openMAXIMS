	private int getNrOfBeds(FloorBedSpaceLayoutLiteVo floorLayout)
	{
		int noOfBeds = 0;
		
		if (floorLayout == null || (floorLayout != null && !floorLayout.getBedSpacesIsNotNull()) ) 
			 return noOfBeds;
		 
		return  noOfBeds = floorLayout.getBedSpaces().size();
	}

