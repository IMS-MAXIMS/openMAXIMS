	{
		if(bedAvailability == null)
			return null;

		if(bedAvailability.getAvailableDateTime() != null)
			return form.getImages().Emergency.Bed_Available;

		if(bedAvailability.getAwaitingDateTime() != null)
			return form.getImages().Emergency.Bed_Awaiting;

		return null;
	}



