	private WardStayVo getAdmissionWardStay(DateTime admissionDate, WardStayVoCollection stays)
	{
		if (admissionDate == null || stays == null)
			return null;
		for (WardStayVo vo : stays)
		{
			if (admissionDate.equals(vo.getTransferDateTime()))
				return vo;
		}
		return null;
	}

