	private WardStay getPreviousOpenWardStay(Set wardStays)
	{
		if (wardStays == null)
			return null;
		
		Iterator<?> it = wardStays.iterator();
		
		while (it.hasNext())
		{
			Object ws = it.next();
			
			if (ws instanceof WardStay)
			{
				if (((WardStay) ws).getTransferOutDateTime() == null)
					return (WardStay) ws;
			}
		}
		return null;		
	}


