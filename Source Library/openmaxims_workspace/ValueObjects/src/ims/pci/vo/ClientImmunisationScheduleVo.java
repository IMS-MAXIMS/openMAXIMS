				status.status = ims.pci.vo.lookups.VaccineStatus.TERMINATEDBYPARENTGUARDIAN;
	
			status.terminatedby = terminationDetails.getTerminatedBy();
			status.terminationreason = terminationDetails.getTerminationReason();
	
