		{	
			str.append(s.substring(0, (s.lastIndexOf(" ", 55) >= 45 && s.lastIndexOf(" ", 55) <= 55 ? s.lastIndexOf(" ", 55) : 50)) + "<br/>");
			s = s.substring(s.lastIndexOf(" ", 55) >= 45 && s.lastIndexOf(" ", 55) <= 55 ? s.lastIndexOf(" ", 55) : 50, s.length());
		}
		return str.append(s).toString();
