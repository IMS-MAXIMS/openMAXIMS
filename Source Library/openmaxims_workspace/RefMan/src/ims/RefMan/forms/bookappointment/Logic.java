	private LocationListVo getHospitalForLocation(LocationListVo location)
	{
		if (location == null)
			return null;

		if (LocationType.HOSP.equals(location.getType()))
			return  location;

		while (location.getParentLocation() != null)
		{
			location = location.getParentLocation();

			if (LocationType.HOSP.equals(location.getType()))
				return  location;

		}

		return  null;
	}

