	private String stripspaces(String addressPostCode)
	{
		if(addressPostCode == null)
			return null;
		StringTokenizer st = new StringTokenizer(addressPostCode);
		String t = "";
		while (st.hasMoreElements())
			t += st.nextElement();
		return t;
	}

	
