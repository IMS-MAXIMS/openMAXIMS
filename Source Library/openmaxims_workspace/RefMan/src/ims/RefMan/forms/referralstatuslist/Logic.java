	private String getMessage(List<String> uiErrors)
	{
		if(uiErrors == null)
			return "";
		
		StringBuffer text = new StringBuffer();
		
		for(int i=0; i<uiErrors.size(); i++)
		{
			text.append(text.length() > 0 ? "\n" : "");
			text.append(uiErrors.get(i));
		}
		
		return text.toString();
	}
	
