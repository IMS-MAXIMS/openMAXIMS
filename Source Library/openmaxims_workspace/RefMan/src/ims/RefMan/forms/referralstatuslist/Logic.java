	private Integer getIndex(Object value)
	{
		if(value == null)
			return null;
		
		for(int i=0; i<form.dyngrdReferrals().getRows().size(); i++)
		{
			if (value.equals(form.dyngrdReferrals().getRows().get(i).getValue()))
				return i;
		}
		
		return null;
	}

