		{	
			referralDetailsVo.setEndDateKPI(getEndDateKPI(doCats, referralDetailsVo));
		}
		
		ReferralLetterDetails doLetterDetail = ReferralLetterDetailsVoAssembler.extractReferralLetterDetails(factory, referralDetailsVo);
		
